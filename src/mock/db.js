// --- SUPABASE STORAGE ADAPTER ABSTRACTION ---
// This adapter seamlessly manages LocalStorage right now, but is structurally 
// isolated so you can instantly switch to Supabase queries in the future.
class StorageAdapter {
    constructor() {
        this.cachePrefix = 'amp_';
        this.schemaVersion = '2.5';
    }

    async initCheck() {
        if (localStorage.getItem(this.cachePrefix + 'schema_version') !== this.schemaVersion) {
            localStorage.clear();
            localStorage.setItem(this.cachePrefix + 'schema_version', this.schemaVersion);
        }
    }

    async fetchCollection(table, fallbackData) {
        // [FUTURE SUPABASE]: const { data } = await supabase.from(table).select('*')
        const stored = JSON.parse(localStorage.getItem(this.cachePrefix + table));
        if (!stored || (Array.isArray(stored) && stored.length === 0)) {
            await this.writeCollection(table, fallbackData);
            return fallbackData;
        }
        return stored;
    }

    async writeCollection(table, data) {
        // [FUTURE SUPABASE]: await supabase.from(table).upsert(data)
        localStorage.setItem(this.cachePrefix + table, JSON.stringify(data));
    }

    async syncAll(state) {
        // Batch sync for optimistic UI
        await this.writeCollection('assets', state.assets);
        await this.writeCollection('grants', state.grants);
        await this.writeCollection('maint', state.maintenanceLogs);
        await this.writeCollection('requests', state.requests);
        await this.writeCollection('transfers', state.transfers);
    }
}

export class AssetDB {
    constructor() {
        this.adapter = new StorageAdapter();
        this.adapter.initCheck();

        // Synchronous initial hydration for immediate UI rendering
        // [FUTURE SUPABASE]: We will shift this to an async initialization lifecycle hook in main.js
        this.assets = JSON.parse(localStorage.getItem('amp_assets'));
        if (!this.assets || this.assets.length < 10 || !this.assets[0].fundingSource) {
            this.assets = INITIAL_ASSETS;
            localStorage.setItem('amp_assets', JSON.stringify(INITIAL_ASSETS));
        }

        this.grants = JSON.parse(localStorage.getItem('amp_grants')) || INITIAL_GRANTS;
        
        let storedMaint = JSON.parse(localStorage.getItem('amp_maint'));
        if (!storedMaint || storedMaint.length === 0) {
            storedMaint = [];
            // Generate some historical, resolved maintenance logs
            this.assets.slice(0, 30).forEach((asset, i) => {
                if (i % 2 === 0) {
                    storedMaint.push({
                        id: 'M-' + (100000 + i * 23),
                        assetId: asset.id,
                        description: `Scheduled preventative maintenance and firmware update. Replaced outer module.`,
                        reporter: 'System Audit',
                        date: new Date(new Date().setMonth(new Date().getMonth() - (i % 6) - 1)).toISOString(),
                        status: 'Resolved'
                    });
                }
                if (i % 3 === 0) {
                    storedMaint.push({
                        id: 'M-' + (200000 + i * 17),
                        assetId: asset.id,
                        description: `User reported spontaneous power cycling. Battery diagnostic run and cell replaced.`,
                        reporter: asset.assignedTo,
                        date: new Date(new Date().setMonth(new Date().getMonth() - (i % 4) - 5)).toISOString(),
                        status: 'Resolved'
                    });
                }
            });
            localStorage.setItem('amp_maint', JSON.stringify(storedMaint));
        }
        this.maintenanceLogs = storedMaint;
        
        this.requests = JSON.parse(localStorage.getItem('amp_requests')) || [];
        
        let storedTransfers = JSON.parse(localStorage.getItem('amp_transfers'));
        if (!storedTransfers || storedTransfers.length < 10) {
            storedTransfers = [];
            // Seed elaborate transfer hops (Chain of Custody)
            this.assets.forEach((asset, i) => {
                const originDate = new Date(asset.purchaseDate);
                if (i % 3 === 0) {
                    const prevEmp = RAW_EMPLOYEES[(i + 5) % RAW_EMPLOYEES.length];
                    storedTransfers.push({
                        id: 'T-' + (10000 + i * 3),
                        assetId: asset.id,
                        fromAssignee: "Central Depot",
                        fromDesignation: "Storage Node",
                        fromLocation: "Warehouse A",
                        toAssignee: prevEmp.name,
                        toDesignation: "Field Officer",
                        toLocation: "Bangalore HQ",
                        date: new Date(originDate.getTime() + 86400000 * 5).toISOString()
                    });
                    storedTransfers.push({
                        id: 'T-' + (10001 + i * 3),
                        assetId: asset.id,
                        fromAssignee: prevEmp.name,
                        fromDesignation: "Field Officer",
                        fromLocation: "Bangalore HQ",
                        toAssignee: asset.assignedTo,
                        toDesignation: asset.assignedToDesignation,
                        toLocation: asset.location,
                        date: new Date(originDate.getTime() + 86400000 * 200).toISOString()
                    });
                } else {
                    storedTransfers.push({
                        id: 'T-' + (20000 + i),
                        assetId: asset.id,
                        fromAssignee: "Central Depot",
                        fromDesignation: "Storage Node",
                        fromLocation: "Warehouse A",
                        toAssignee: asset.assignedTo,
                        toDesignation: asset.assignedToDesignation,
                        toLocation: asset.location,
                        date: originDate.toISOString()
                    });
                }
            });
            localStorage.setItem('amp_transfers', JSON.stringify(storedTransfers));
        }
        this.transfers = storedTransfers;
    }

    // This method effectively models an async network push to Supabase.
    // It is called in the background after UI state mutations.
    async syncToCloud() {
        await this.adapter.syncAll({
            assets: this.assets,
            grants: this.grants,
            maintenanceLogs: this.maintenanceLogs,
            requests: this.requests,
            transfers: this.transfers
        });
    }

    reportIssue(assetId, description, reporter) {
        const asset = this.assets.find(a => a.id === assetId);
        if (asset) {
            asset.status = "Maintenance";
            asset.health = "Requires Check";
        }
        const log = {
            id: 'M-' + Date.now(),
            assetId,
            description,
            reporter,
            date: new Date().toISOString(),
            status: 'Pending'
        };
        this.maintenanceLogs.unshift(log);
        this.syncToCloud();
        return log;
    }

    requestAsset(category, reason, user) {
        const request = {
            id: 'REQ-' + Date.now(),
            category,
            reason,
            user,
            date: new Date().toISOString(),
            status: 'Pending Both',
            managerApproved: false,
            financeApproved: false
        };
        this.requests.unshift(request);
        this.syncToCloud();
        return request;
    }

    getStats() {
        return {
            totalAssets: this.assets.length,
            activeAssets: this.assets.filter(a => a.status === 'Active').length,
            maintenanceAssets: this.assets.filter(a => a.status === 'Maintenance').length,
            totalValue: this.assets.reduce((sum, a) => sum + a.amount, 0),
            netValue: this.assets.reduce((sum, a) => sum + (a.amount - a.depreciation), 0)
        };
    }

    _evaluateRequestStatus(req) {
        if (req.status.startsWith('Rejected')) return;
        if (req.managerApproved && req.financeApproved) {
            req.status = "Approved";
        } else if (req.managerApproved && !req.financeApproved) {
            req.status = "Pending Finance";
        } else if (!req.managerApproved && req.financeApproved) {
            req.status = "Pending Manager";
        } else {
            req.status = "Pending Both";
        }
    }

    approveRequestManager(requestId) {
        const req = this.requests.find(r => r.id === requestId);
        if (req && !req.status.startsWith('Rejected')) {
            req.managerApproved = true;
            this._evaluateRequestStatus(req);
            this.syncToCloud();
        }
        return req;
    }

    approveRequestFinance(requestId) {
        const req = this.requests.find(r => r.id === requestId);
        if (req && !req.status.startsWith('Rejected')) {
            req.financeApproved = true;
            this._evaluateRequestStatus(req);
            this.syncToCloud();
        }
        return req;
    }

    rejectRequestManager(requestId) {
        const req = this.requests.find(r => r.id === requestId);
        if (req) {
            req.status = "Rejected by Manager";
            this.syncToCloud();
        }
        return req;
    }

    rejectRequestFinance(requestId) {
        const req = this.requests.find(r => r.id === requestId);
        if (req) {
            req.status = "Rejected by Finance";
            this.syncToCloud();
        }
        return req;
    }

    addNewAsset(assetData) {
        const newAsset = {
            id: 'AS-' + Math.floor(10000 + Math.random() * 90000),
            name: assetData.name,
            category: assetData.category,
            status: assetData.status || "Active",
            location: assetData.location,
            health: "100.0%",
            purchaseDate: new Date().toISOString().split('T')[0],
            amount: parseFloat(assetData.amount) || 0,
            program: assetData.program || "General Fleet",
            assignedTo: assetData.assignedTo || "Unassigned",
            assignedToId: assetData.assignedToId || "N/A",
            assignedToDesignation: assetData.designation || "N/A",
            depreciation: 0,
            fundingSource: assetData.fundingSource || "General Fund",
            fundingAmount: parseFloat(assetData.amount) || 0
        };
        this.assets.unshift(newAsset);
        this.syncToCloud();
        return newAsset;
    }

    updateAsset(id, assetData) {
        const index = this.assets.findIndex(a => a.id === id);
        if (index !== -1) {
            this.assets[index] = { ...this.assets[index], ...assetData };
            this.syncToCloud();
            return this.assets[index];
        }
        return null;
    }

    deleteAsset(id) {
        this.assets = this.assets.filter(a => a.id !== id);
        // Also remove associated transfers and requests to clean up
        this.transfers = this.transfers.filter(t => t.assetId !== id);
        this.requests = this.requests.filter(r => r.assetId !== id);
        this.syncToCloud();
    }

    transferAsset(assetId, newAssignee, newLocation, newDesignation = "N/A", newAssigneeId = "N/A") {
        const asset = this.assets.find(a => a.id === assetId);
        if (asset) {
            const tx = {
                id: 'T-' + Date.now(),
                assetId: asset.id,
                fromAssignee: asset.assignedTo,
                fromDesignation: asset.assignedToDesignation,
                fromLocation: asset.location,
                toAssignee: newAssignee || asset.assignedTo,
                toDesignation: newDesignation || asset.assignedToDesignation,
                toLocation: newLocation || asset.location,
                date: new Date().toISOString()
            };
            
            this.transfers.unshift(tx);
            
            if (newAssignee) {
                asset.assignedTo = newAssignee;
                asset.assignedToDesignation = newDesignation;
                asset.assignedToId = newAssigneeId;
            }
            if (newLocation) asset.location = newLocation;
            this.syncToCloud();
        }
        return asset;
    }
}

export const db = new AssetDB();
